"""Internacionalización ligera de la interfaz.

El IDIOMA BASE es el español: las CLAVES del catálogo son las cadenas en español tal
cual aparecen en el código. ``tr(texto)`` devuelve la traducción del idioma activo, o la
propia cadena española si no hay traducción (degradación elegante, nunca rompe).

Uso:
    from ..i18n import tr
    boton.setText(tr("Buscar"))
    label.setText(tr("Nexus Mod Installer — {game}").format(game=g.name))

El idioma se fija una vez al arrancar (``set_language(config.language)`` en main.py) y se
cambia desde Ajustes (se aplica al reiniciar).

NOTA: el catálogo (_CATALOG) está GENERADO. Para regenerarlo/ampliarlo, edita la lista de
cadenas y vuelve a lanzar el flujo de traducción.
"""
from __future__ import annotations

# Idiomas disponibles: código -> nombre nativo (para el selector).
LANGUAGES = {
    "es": "Español",
    "en": "English",
    "fr": "Français",
    "de": "Deutsch",
    "it": "Italiano",
}

_lang = "es"


def set_language(code: str) -> None:
    global _lang
    _lang = code if code in LANGUAGES else "es"


def get_language() -> str:
    return _lang


def language_name(code: str) -> str:
    return LANGUAGES.get(code, code)


def tr(text: str) -> str:
    """Traduce ``text`` al idioma activo (o lo devuelve igual si es español/sin traducir)."""
    if _lang == "es":
        return text
    return _CATALOG.get(_lang, {}).get(text, text)


